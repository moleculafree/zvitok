CREATE TABLE SPR_DOC
(
    DOC STRING (6) NOT NULL,
    NAME TEXT,
    CNT  BOOLEAN DEFAULT (0),
    PARENT STRING (6),
    CONSTRAINT PK_SPR_DOC PRIMARY KEY (DOC COLLATE NOCASE ASC, PARENT COLLATE NOCASE ASC) ON CONFLICT REPLACE
);

CREATE UNIQUE INDEX SPR_DOC_IND ON SPR_DOC (DOC COLLATE NOCASE ASC, PARENT COLLATE NOCASE ASC);

CREATE TABLE SPR_TERM
(
    DOC         TEXT(8)    NOT NULL,
    PERIOD TEXT (6) NOT NULL,
    PERIOD_TYPE INTEGER(1) NOT NULL,
    TERM        DATE       NOT NULL,
    PRIMARY KEY (DOC, PERIOD, PERIOD_TYPE)
);

CREATE UNIQUE INDEX TERM_DOC_IND ON SPR_TERM (DOC COLLATE NOCASE ASC, PERIOD COLLATE NOCASE ASC, PERIOD_TYPE COLLATE NOCASE ASC);

CREATE TABLE SPR_VER
(
    DOC    TEXT(8) PRIMARY KEY NOT NULL UNIQUE,
    PBEGIN DATE,
    PEND   DATE,
    DBEGIN DATE,
    DEND   DATE
);

CREATE UNIQUE INDEX VER_DOC_IND ON SPR_VER (DOC COLLATE NOCASE ASC);