ALTER TABLE ZVIT RENAME TO ZVIT_old;

CREATE TABLE ZVIT
(
    id         INTEGER PRIMARY KEY,
    parent_id  INTEGER DEFAULT NULL,
    NAME       TEXT     NOT NULL,
    TIME       DATETIME NOT NULL,
    PERIOD     TEXT(6),
    DATA       TEXT     NOT NULL,
    PARENT     TEXT,
    STATUS     TEXT,
    deleted    INTEGER DEFAULT 0,
    deleted_at TEXT
);

INSERT INTO ZVIT (NAME, TIME, PERIOD, DATA, PARENT, STATUS)
SELECT NAME, TIME, PERIOD, DATA, PARENT, STATUS
FROM ZVIT_old
ORDER BY ROWID;

UPDATE ZVIT
SET parent_id = COALESCE((SELECT id FROM ZVIT AS p WHERE p.NAME = ZVIT.PARENT LIMIT 1), -1)
WHERE PARENT IS NOT NULL
  AND PARENT != '';

DROP TABLE ZVIT_old;

CREATE INDEX IF NOT EXISTS idx_zvit_active ON ZVIT (deleted);
CREATE INDEX IF NOT EXISTS idx_zvit_parent ON ZVIT (parent_id);
